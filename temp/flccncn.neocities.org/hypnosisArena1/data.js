function replaceCustomStructureValues(obj) {
    Object.keys(obj).forEach(key => {
        if (typeof obj[key] === 'string') {
            if (obj[key] === 'variableList') {
                obj[key] = variableList;
            } else if (obj[key] === 'variableData') {
                obj[key] = deepCloneAndModify(variableData, obj.id);
            }
        } else if (typeof obj[key] === 'object' && obj[key] !== null) {
            replaceCustomStructureValues(obj[key]);
        }
    });
}

function deepCloneAndModify(data, parentId) {
    return data.map(item => {
        if (typeof item === 'object' && item !== null) {
            const newItem = Array.isArray(item) ? [] : { ...item };
            if (newItem.id) {
                newItem.id = parentId + newItem.id;
            }

            Object.keys(newItem).forEach(key => {
                if (Array.isArray(newItem[key])) {
                    newItem[key] = deepCloneAndModify(newItem[key], parentId);
                } else if (typeof newItem[key] === 'object' && newItem[key] !== null) {
                    newItem[key] = deepCloneAndModify([newItem[key]], parentId)[0];
                }
            });

            return newItem;
        } else {
            return item;
        }
    });
}

